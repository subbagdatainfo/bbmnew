                   <div class="row-md-4">
                        <p>Antonius Wahyudi Sutrisna atau yang dikenal sebagai Dedek Wahyudi. Lahir pada tahun 1960 di Klaten, Jawa Tengah. Dedek dikenal sebagai musikus dan penata musik untuk ilustrasi drama, wayang, dan tari-tarian tradisional dari Surakarta. Dedek juga dikenal sebagai salah satu komposer musik gamelan, khususnya dalam permainan bonangnya. Selain sebagai seorang komposer Dedek juga seorang pengrawit yang pandai memainkan instrumen. </p>
                        <p>Kehebatan Dedek dalam bidang seni ini tidak hanya diakui di tingkat nasional saja. Namanya juga bersinar di tingkat internasional. Terbukti dengan diundangnya Dedek ke acara internasional sebagai komposer tamu oleh Gamelan Pasifica Group di Seattle, Amerika Serikat pada tahun 1994. Beberapa kali pula, karya-karyanya dipentaskan, salah satunya adalah dalam acara World and Musik Festival di Inggris tahun 1997. Selain itu, Dedek juga sering melakukan kolaborasi dengan pemusik lintas budaya, salah satunya dalam program Asia Pasific Performing Exchange di Los Angeles, Amerika Serikat pada tahun 2006.</p>
